package Bean;

public class vocabulary {
	private int idvocabulary;
	private String vocabularyname;
	private String vocabularyimage;
	private int checktable;
	public int getIdvocabulary() {
		return idvocabulary;
	}
	public void setIdvocabulary(int idvocabulary) {
		this.idvocabulary = idvocabulary;
	}
	public String getVocabularyname() {
		return vocabularyname;
	}
	public void setVocabularyname(String vocabularyname) {
		this.vocabularyname = vocabularyname;
	}
	public String getVocabularyimage() {
		return vocabularyimage;
	}
	public void setVocabularyimage(String vocabularyimage) {
		this.vocabularyimage = vocabularyimage;
	}
	public int getChecktable() {
		return checktable;
	}
	public void setChecktable(int checktable) {
		this.checktable = checktable;
	}
}
